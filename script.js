// Current language
let currentLang = "id";

// DOM elements
const themeToggle = document.getElementById("themeToggle");
const langToggle = document.getElementById("langToggle");
const musicToggle = document.getElementById("musicToggle");
const backToTop = document.getElementById("backToTop");
const backgroundMusic = document.getElementById("backgroundMusic");
const hamburger = document.querySelector(".hamburger");
const navMenu = document.querySelector(".nav-menu");

// Initialize
document.addEventListener("DOMContentLoaded", () => {
  // Load saved theme
  const savedTheme = localStorage.getItem("theme") || "light";
  const savedLang = localStorage.getItem("language") || "id";

  setTheme(savedTheme);
  setLanguage(savedLang);

  // Initialize music state - TAMBAHKAN INI
  initializeMusicState();

  // Add loading animation
  const elements = document.querySelectorAll(
    ".feature-card, .menu-card, .value-card, .team-member"
  );
  elements.forEach((el, index) => {
    el.classList.add("loading");
    setTimeout(() => {
      el.classList.add("loaded");
    }, index * 100);
  });

  // Initialize music
  if (backgroundMusic) {
    backgroundMusic.volume = 0.3;
  }
});

// Theme toggle
themeToggle.addEventListener("click", () => {
  const currentTheme = document.documentElement.getAttribute("data-theme");
  const newTheme = currentTheme === "dark" ? "light" : "dark";
  setTheme(newTheme);
});

function setTheme(theme) {
  document.documentElement.setAttribute("data-theme", theme);
  themeToggle.textContent = theme === "dark" ? "☀️" : "🌙";
  localStorage.setItem("theme", theme);
}

// Language toggle
langToggle.addEventListener("click", () => {
  const newLang = currentLang === "id" ? "en" : "id";
  setLanguage(newLang);
});

function setLanguage(lang) {
  currentLang = lang;
  langToggle.textContent = lang === "id" ? "EN" : "ID";
  document.documentElement.setAttribute("lang", lang);

  // Update all text elements
  const elements = document.querySelectorAll("[data-id]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-id");
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });

  localStorage.setItem("language", lang);
}

// Music state management
let isPlaying = false;
let currentTime = 0;
let musicVolume = 0.3;

// Initialize music state from localStorage
function initializeMusicState() {
  const savedMusicState = localStorage.getItem("musicState");
  const savedMusicTime = localStorage.getItem("musicTime");
  const savedMusicVolume = localStorage.getItem("musicVolume");

  if (savedMusicState === "playing") {
    isPlaying = true;
    musicToggle.classList.add("playing");
    musicToggle.textContent = "🎶";
  }

  if (savedMusicTime) {
    currentTime = Number.parseFloat(savedMusicTime);
  }

  if (savedMusicVolume) {
    musicVolume = Number.parseFloat(savedMusicVolume);
  }

  if (backgroundMusic) {
    backgroundMusic.volume = musicVolume;
    backgroundMusic.currentTime = currentTime;

    if (isPlaying) {
      // Small delay to ensure page is loaded
      setTimeout(() => {
        backgroundMusic.play().catch((e) => {
          console.log("Auto-play failed:", e);
          // Reset state if autoplay fails
          isPlaying = false;
          musicToggle.classList.remove("playing");
          musicToggle.textContent = "🎵";
          localStorage.setItem("musicState", "paused");
        });
      }, 100);
    }
  }
}

// Save music state periodically
function saveMusicState() {
  if (backgroundMusic && !backgroundMusic.paused) {
    localStorage.setItem("musicTime", backgroundMusic.currentTime.toString());
    localStorage.setItem("musicVolume", backgroundMusic.volume.toString());
  }
}

// Music toggle with state persistence
musicToggle.addEventListener("click", () => {
  if (backgroundMusic) {
    if (isPlaying) {
      backgroundMusic.pause();
      musicToggle.classList.remove("playing");
      musicToggle.textContent = "🎵";
      localStorage.setItem("musicState", "paused");
      localStorage.setItem("musicTime", backgroundMusic.currentTime.toString());
    } else {
      backgroundMusic.play().catch((e) => {
        console.log("Audio play failed:", e);
      });
      musicToggle.classList.add("playing");
      musicToggle.textContent = "🎶";
      localStorage.setItem("musicState", "playing");
    }
    isPlaying = !isPlaying;
  }
});

// Save music time periodically while playing
setInterval(() => {
  if (backgroundMusic && !backgroundMusic.paused) {
    saveMusicState();
  }
}, 1000);

// Save state before page unload
window.addEventListener("beforeunload", () => {
  saveMusicState();
});

// Handle music end (loop)
if (backgroundMusic) {
  backgroundMusic.addEventListener("ended", () => {
    backgroundMusic.currentTime = 0;
    if (isPlaying) {
      backgroundMusic.play();
    }
  });

  // Update time tracking
  backgroundMusic.addEventListener("timeupdate", () => {
    currentTime = backgroundMusic.currentTime;
  });
}

// Back to top functionality
window.addEventListener("scroll", () => {
  if (window.pageYOffset > 300) {
    backToTop.classList.add("visible");
  } else {
    backToTop.classList.remove("visible");
  }
});

backToTop.addEventListener("click", () => {
  window.scrollTo({
    top: 0,
    behavior: "smooth",
  });
});

// Mobile menu toggle
hamburger.addEventListener("click", (e) => {
  e.stopPropagation();
  hamburger.classList.toggle("active");
  navMenu.classList.toggle("active");

  // Prevent body scroll when menu is open
  if (navMenu.classList.contains("active")) {
    document.body.style.overflow = "hidden";
  } else {
    document.body.style.overflow = "auto";
  }
});

// Close mobile menu when clicking outside
document.addEventListener("click", (e) => {
  if (!navMenu.contains(e.target) && !hamburger.contains(e.target)) {
    hamburger.classList.remove("active");
    navMenu.classList.remove("active");
    document.body.style.overflow = "auto";
  }
});

// Close mobile menu when clicking on a link
document.querySelectorAll(".nav-menu a").forEach((link) => {
  link.addEventListener("click", () => {
    hamburger.classList.remove("active");
    navMenu.classList.remove("active");
    document.body.style.overflow = "auto";
  });
});

// Close mobile menu on window resize
window.addEventListener("resize", () => {
  if (window.innerWidth > 768) {
    hamburger.classList.remove("active");
    navMenu.classList.remove("active");
    document.body.style.overflow = "auto";
  }
});

// Smooth scroll for anchor links
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", function (e) {
    e.preventDefault();
    const target = document.querySelector(this.getAttribute("href"));
    if (target) {
      target.scrollIntoView({
        behavior: "smooth",
        block: "start",
      });
    }
  });
});

// Add scroll animations
const observerOptions = {
  threshold: 0.1,
  rootMargin: "0px 0px -50px 0px",
};

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("loaded");
    }
  });
}, observerOptions);

// Observe elements for animation
document
  .querySelectorAll(
    ".feature-card, .menu-card, .value-card, .team-member, .menu-item"
  )
  .forEach((el) => {
    observer.observe(el);
  });

// Add hover effects for menu items
document.querySelectorAll(".menu-card, .menu-item").forEach((item) => {
  item.addEventListener("mouseenter", function () {
    this.style.transform = "translateY(-5px) scale(1.02)";
  });

  item.addEventListener("mouseleave", function () {
    this.style.transform = "translateY(0) scale(1)";
  });
});

// Add click effects for buttons
document.querySelectorAll("button, .cta-button").forEach((button) => {
  button.addEventListener("click", function (e) {
    const ripple = document.createElement("span");
    const rect = this.getBoundingClientRect();
    const size = Math.max(rect.width, rect.height);
    const x = e.clientX - rect.left - size / 2;
    const y = e.clientY - rect.top - size / 2;

    ripple.style.width = ripple.style.height = size + "px";
    ripple.style.left = x + "px";
    ripple.style.top = y + "px";
    ripple.classList.add("ripple");

    this.appendChild(ripple);

    setTimeout(() => {
      ripple.remove();
    }, 600);
  });
});

// Add ripple effect CSS
const style = document.createElement("style");
style.textContent = `
    .ripple {
        position: absolute;
        border-radius: 50%;
        background: rgba(255, 255, 255, 0.6);
        transform: scale(0);
        animation: ripple-animation 0.6s linear;
        pointer-events: none;
    }
    
    @keyframes ripple-animation {
        to {
            transform: scale(4);
            opacity: 0;
        }
    }
    
    button, .cta-button {
        position: relative;
        overflow: hidden;
    }
`;
document.head.appendChild(style);

// Preload images
function preloadImages() {
  const images = document.querySelectorAll('img[src*="placeholder"]');
  images.forEach((img) => {
    const newImg = new Image();
    newImg.src = img.src;
  });
}

// Call preload on page load
window.addEventListener("load", preloadImages);

// Add keyboard navigation
document.addEventListener("keydown", (e) => {
  // ESC key closes mobile menu
  if (e.key === "Escape") {
    hamburger.classList.remove("active");
    navMenu.classList.remove("active");
  }

  // Space bar toggles music
  if (e.code === "Space" && e.target === document.body) {
    e.preventDefault();
    musicToggle.click();
  }

  // Home key goes to top
  if (e.key === "Home") {
    e.preventDefault();
    backToTop.click();
  }
});

// Add performance optimization
let ticking = false;

function updateScrollElements() {
  // Update back to top button
  if (window.pageYOffset > 300) {
    backToTop.classList.add("visible");
  } else {
    backToTop.classList.remove("visible");
  }

  ticking = false;
}

window.addEventListener("scroll", () => {
  if (!ticking) {
    requestAnimationFrame(updateScrollElements);
    ticking = true;
  }
});
